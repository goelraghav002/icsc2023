                    <p class="text-justify">
                    The Department of Electronics and Communication Engineering (ECE) at the Jaypee Institute of Information Technology (JIIT) is proud to host the 9th International Conference on Signal Processing and Communication (ICSC 2023). As a leading academic department, we are dedicated to promoting research, innovation, and knowledge exchange in the field of electrical and computer engineering. <br>
                    At JIIT's ECE Department, we take pride in our world-class faculty members who are renowned experts in their respective fields. They bring a wealth of knowledge, expertise, and research experience to the department. Our faculty members actively engage in cutting-edge research in areas such as signal processing, communications, image and video processing, machine learning, and wireless networks, among others. Their research contributions have been published in prestigious international journals and presented at renowned conferences. <br><br>
                    We believe in nurturing the next generation of engineering professionals through a holistic approach to education. Our curriculum is designed to provide a strong foundation in electrical and computer engineering principles while incorporating the latest advancements in the field. Students at our department have access to state-of-the-art laboratories and facilities, enabling hands-on experiences in designing and implementing signal processing and communication systems. Through practical projects, internships, and industry collaborations, we ensure that our students develop essential skills that are in high demand in the industry.<br><br>
                    ICSC 2023 serves as an excellent platform for researchers, academicians, and industry professionals from around the globe to exchange ideas, discuss emerging trends, and showcase innovative research outcomes. The conference will feature keynote lectures by distinguished experts in the field, paper presentations, and interactive sessions, facilitating knowledge dissemination and fostering collaborations. Participants will have the opportunity to explore the latest advancements in signal processing and communication technologies, engage in insightful discussions, and establish valuable connections with fellow researchers and industry professionals.<br><br>
                    As the host of ICSC 2023, the ECE Department at JIIT is committed to ensuring a successful and impactful conference. We have assembled a dedicated organizing committee that will work tirelessly to curate a rich and diverse program. The conference will cover a wide range of topics including but not limited to digital signal processing, wireless communication, multimedia systems, network security, and machine learning for signal processing. We encourage researchers to submit their original research papers to contribute to the conference proceedings and share their findings with the international community.
<br><br>
We invite participants from academia, industry, and research organizations to join us at ICSC 2023 for an intellectually stimulating and enriching experience. With its beautiful campus, state-of-the-art facilities, and vibrant academic community, JIIT provides an ideal setting for fostering collaborations and advancing knowledge in the field of signal processing and communication.<br><br>
We look forward to welcoming participants from around the world to ICSC 2023 and creating a memorable and impactful conference that will contribute to the advancement of signal processing and communication technologies.<br><br>
<span class="text-2xl dark:">Tracks</span><br>   <br>
Track 1: Communication<br><br>
The Communication track focuses on the latest developments in wireless communication, optical communication, satellite communication, mobile and cellular communication, network protocols, information theory, coding techniques, and cognitive radio. We welcome researchers and experts to submit their contributions and shed light on emerging trends and breakthroughs in communication technologies.<br>   <br>
Track 2: Signal Processing<br>   <br>
The Signal Processing track invites submissions on various topics such as digital signal processing, image and video processing, audio processing, biomedical signal processing, pattern recognition, signal analysis and modeling, signal compression and coding, and statistical signal processing. Researchers are encouraged to share their findings and innovations in these exciting areas<br>   <br>
Track 3: VLSI and Embedded Technology<br>   <br>
The VLSI and Embedded Technology track provides a platform for researchers to showcase their work in VLSI design methodologies, system-on-chip (SoC) architectures, hardware-software co-design, low-power VLSI design, reconfigurable computing, embedded systems design, FPGA-based systems, and testing and verification of VLSI circuits. We invite scholars and professionals to present their novel approaches and advancements in this rapidly evolving field.<br>   <br>
Track 4: Intelligent Computing and Machine Learning<br>   <br>
The Intelligent Computing and Machine Learning track aims to gather cutting-edge research in artificial intelligence, machine learning algorithms and models, deep learning, natural language processing, data mining and knowledge discovery, intelligent systems and applications, swarm intelligence, and evolutionary computation. <br>   <br>

